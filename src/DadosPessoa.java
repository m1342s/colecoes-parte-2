public class DadosPessoa {
    public String nome;
    public String sexo;
    public DadosPessoa(String nome, String sexo) {
        this.nome = nome;
        this.sexo = sexo;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getNome() {
        return nome;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;

    }
    public String getSexo() {
        return sexo;
    }


}
